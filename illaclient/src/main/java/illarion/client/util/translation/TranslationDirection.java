package illarion.client.util.translation;

/**
 * This enumeration defines the possible directions for a translation.
 *
 * @author Martin Karing &lt;nitram@illarion.org&gt;
 */
public enum TranslationDirection {
    /**
     * Translate a English text to it's German version.
     */
    EnglishToGerman,

    /**
     * Translate a German text to it's English version.
     */
    GermanToEnglish
}
